export type Dict = typeof dictionaries.ru;

function getNested(obj: Record<string, unknown>, path: string): string | undefined {
  const parts = path.split(".");
  let cur: unknown = obj;
  for (const p of parts) {
    if (cur == null || typeof cur !== "object") return undefined;
    cur = (cur as Record<string, unknown>)[p];
  }
  return typeof cur === "string" ? cur : undefined;
}

export function translate(locale: Locale, key: string): string {
  const dict = locale === "ru" ? dictionaries.ru : dictionaries.en;
  const raw = getNested(dict as unknown as Record<string, unknown>, key);
  if (raw) return raw;
  const fallback = getNested(dictionaries.en as unknown as Record<string, unknown>, key);
  return fallback ?? key;
}
